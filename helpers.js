export const findClosest = (val, arr) => {
    return arr.reduce((a, b) => {
        let aDiff = Math.abs(a - val)
        let bDiff = Math.abs(b - val)
        if(aDiff == bDiff){
            return a > b ? a : b
        } else {
            return bDiff < aDiff ? b : a
        }
    })
}

